function selectpage(p)
{
    p = p || window.event;
    
    var elements = document.getElementsByClassName('affiliate_page_content');
    
    for(var i = 0; i < elements.length; i++)
    {
        elements[i].style.display = 'none'; // Hide all elements.
    }
    
    document.getElementById("affiliate_page_content_" + p).style.display = 'inline';
    
    var buttons = document.getElementsByClassName('affiliate_menu_button');
    
    for(var j = 0; j < buttons.length; j++)
    {
        buttons[j].style.border = 'outset'; // Set border to outset for all buttons.
        buttons[j].style.background = 'teal'; // Set background to teal.
    }
    
    document.getElementById("affiliate_menu_button_" + p).style.border = 'inset';
    document.getElementById("affiliate_menu_button_" + p).style.background = 'gray';
    
    //e.target.style.display = 'inline'; // Show the clicked element.
}
